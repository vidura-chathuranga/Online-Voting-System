            <p>The National Film Awards is the most prominent film award ceremony in India. Established in 1954, it has been administered, along with the International Film Festival of India and the Indian Panorama, by the Indian government's Directorate of Film Festivals since 1973.[2][3]

Every year, a national panel appointed by the government selects the winning entry, and the award ceremony is held in New Delhi, where the President of India presents the awards. This is followed by the inauguration of the National Film Festival, where the award-winning films are screened for the public. Declared for films produced in the previous year across the country, they hold the distinction of awarding merit to the best of Indian cinema overall, as well as presenting awards for the best films in each region and language of the country.</P>
